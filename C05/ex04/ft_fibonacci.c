#include <unistd.h>
#include <stdio.h>
#include <string.h>

int	ft_fibonacci(int index)
{
	if (index < 0)
		return (-1);
	if (index < 2)
		return (index);
	return (ft_fibonacci (index - 1) + ft_fibonacci (index - 2));
}
